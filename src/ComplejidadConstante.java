/**
* Complejidad: O(1)
* Razón: No importa el tamaño de los datos,
* la operación siempre tarda lo mismo.
* 
*/

public class ComplejidadConstante {
    public void ejemplo() {
        System.out.println("Ejemplo de Complejidad Constante --> O(1):");

        int x = 10;
        int y = x + 5;

        System.out.println("Resultado: " + y);
    }
}
